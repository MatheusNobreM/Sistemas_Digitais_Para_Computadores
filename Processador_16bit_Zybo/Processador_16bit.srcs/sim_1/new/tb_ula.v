`timescale 1ns / 1ps

module tb_ula();

    reg  [15:0] A;
    reg  [15:0] B;
    reg  [4:0]  shamt; 
    reg  [3:0]  op;

    wire [15:0] Q;
    wire        Z;     
    wire        C;    

    
    ula dut (
        .A(A),
        .B(B),
        .shamt(shamt),
        .op(op),
        .Q(Q),
        .Z(Z),
        .C(C)
    );

    initial begin

        // --------------------------------------------------
        // Inicialização
        // --------------------------------------------------
        A = 16'd0; B = 16'd0; shamt = 5'd0; op = 4'b0000;
        #10;

        // --------------------------------------------------
        // Teste 1: PASS A (op=0000) - Q deve ser A=0
        // --------------------------------------------------
        A = 16'd7; op = 4'b0000;
        #10; // Q == 7

        // --------------------------------------------------
        // Teste 2: ADD (op=0100) - 15 + 10 = 25
        // --------------------------------------------------
        A = 16'd15; B = 16'd10; op = 4'b0100;
        #10; // Q == 25

        // --------------------------------------------------
        // Teste 3: SUB (op=0101) - 50 - 20 = 30
        // --------------------------------------------------
        A = 16'd50; B = 16'd20; op = 4'b0101;
        #10; // Q == 30

        // --------------------------------------------------
        // Teste 4: MUL (op=0110) - 5 * 4 = 20
        // --------------------------------------------------
        A = 16'd5; B = 16'd4; op = 4'b0110;
        #10; // Q == 20

        // --------------------------------------------------
        // Teste 5: AND lógico (op=0111)
        // --------------------------------------------------
        A = 16'b1100_1100_1100_1100;
        B = 16'b1010_1010_1010_1010;
        op = 4'b0111;
        #10; // Q == 1000_1000_1000_1000

        // --------------------------------------------------
        // Teste 6: NOT lógico (op=1001) - inverte A
        // --------------------------------------------------
        A = 16'b1111_0000_1111_0000; B = 16'd0;
        op = 4'b1001;
        #10; // Q == 0000_1111_0000_1111

        // --------------------------------------------------
        // Teste 7: XOR lógico (op=1010)
        // --------------------------------------------------
        A = 16'b1111_1111_0000_0000;
        B = 16'b1111_0000_1111_0000;
        op = 4'b1010;
        #10; // Q == 0000_1111_1111_0000

        // --------------------------------------------------
        // Teste 8: CMP (op=1011) - A == B → Z=1, C=0
        // --------------------------------------------------
        A = 16'd42; B = 16'd42; op = 4'b1011;
        #10;
        $display("CMP igual:  Z=%b (esperado 1), C=%b (esperado 0)", Z, C);

        // --------------------------------------------------
        // Teste 9: CMP (op=1011) - A < B → Z=0, C=1
        // --------------------------------------------------
        A = 16'd10; B = 16'd20; op = 4'b1011;
        #10;
        $display("CMP menor:  Z=%b (esperado 0), C=%b (esperado 1)", Z, C);

        // --------------------------------------------------
        // Teste 10: CMP (op=1011) - A > B → Z=0, C=0
        // --------------------------------------------------
        A = 16'd30; B = 16'd5; op = 4'b1011;
        #10;
        $display("CMP maior:  Z=%b (esperado 0), C=%b (esperado 0)", Z, C);

        // --------------------------------------------------
        // Teste 11: SHL (op=1100) - 1 << 3 = 8
        // --------------------------------------------------
        A = 16'd1; shamt = 5'd3; op = 4'b1100;
        #10; // Q == 8

        // --------------------------------------------------
        // Teste 12: SHR (op=1101) - 16 >> 2 = 4
        // --------------------------------------------------
        A = 16'd16; shamt = 5'd2; op = 4'b1101;
        #10; // Q == 4

        // --------------------------------------------------
        // Teste 13: ROL (op=1110) - rotaciona 1 bit à esquerda
        // --------------------------------------------------
        A = 16'b1000_0000_0000_0001; op = 4'b1110;
        #10; // Q == 0000_0000_0000_0011 (bit 15 vai para bit 0)

        // --------------------------------------------------
        // Teste 14: ROR (op=1111) - rotaciona 1 bit à direita
        // --------------------------------------------------
        A = 16'b1000_0000_0000_0001; op = 4'b1111;
        #10; // Q == 1100_0000_0000_0000 (bit 0 vai para bit 15)

        $display("Testes da ULA finalizados com sucesso!");
        $finish;
    end

endmodule